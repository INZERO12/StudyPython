# Escape 문자
print('Life is short. You need Python.')
print('Life is short.\rYou need Python.')     # \r 앞으로 땡기기
print('Life is short.\nYou need Python.')     # \n 밑으로 내리기
print('Life is short. You \tneed Python.')    # \t tab
print('Life is short. You need \'Python\'.')  # \'단어\' 
print('Life is short. You\b need Python.')    # \b 한 줄 앞으로
print('Life is \"short\". You need Python.')  #\"단어\"
